# Funkcja do tworzenia pre-commit hook
create_pre_commit_hook() {
    mkdir -p .git/hooks
    cat > .git/hooks/pre-commit << 'HOOK_EOF'
#!/bin/bash

# Pre-commit hook to prevent committing .env and CLAUDE.md files

RED='\033[0;31m'
YELLOW='\033[1;33m'
GREEN='\033[0;32m'
NC='\033[0m'

# Check for .env files in staged changes
ENV_FILES=$(git diff --cached --name-only | grep -E '(^|/)\.env(\.|$)|\.env$')

if [ ! -z "$ENV_FILES" ]; then
    echo -e "${RED}‚ùå B≈ÅƒÑD: Pr√≥bujesz commitowaƒá pliki .env!${NC}"
    echo -e "${RED}Znalezione pliki:${NC}"
    echo "$ENV_FILES" | while read -r file; do
        echo -e "${YELLOW}  - $file${NC}"
    done
    echo ""
    echo -e "${YELLOW}üí° Wskaz√≥wka: Usu≈Ñ te pliki ze stage area u≈ºywajƒÖc:${NC}"
    echo "$ENV_FILES" | while read -r file; do
        echo "   git reset HEAD $file"
    done
    echo ""
    echo -e "${RED}Commit zosta≈Ç anulowany ze wzglƒôd√≥w bezpiecze≈Ñstwa.${NC}"
    exit 1
fi

# Check for CLAUDE.md files in staged changes
CLAUDE_FILES=$(git diff --cached --name-only | grep -iE '(^|/)claude\.md$')

if [ ! -z "$CLAUDE_FILES" ]; then
    echo -e "${RED}‚ùå B≈ÅƒÑD: Pr√≥bujesz commitowaƒá pliki CLAUDE.md!${NC}"
    echo -e "${RED}Te pliki zawierajƒÖ prywatne instrukcje projektu i nie powinny byƒá publiczne.${NC}"
    echo -e "${RED}Znalezione pliki:${NC}"
    echo "$CLAUDE_FILES" | while read -r file; do
        echo -e "${YELLOW}  - $file${NC}"
    done
    echo ""
    echo -e "${YELLOW}üí° Wskaz√≥wka: Usu≈Ñ te pliki ze stage area u≈ºywajƒÖc:${NC}"
    echo "$CLAUDE_FILES" | while read -r file; do
        echo "   git reset HEAD $file"
    done
    echo ""
    echo -e "${RED}Commit zosta≈Ç anulowany ze wzglƒôd√≥w bezpiecze≈Ñstwa.${NC}"
    exit 1
fi

exit 0
HOOK_EOF
    
    chmod +x .git/hooks/pre-commit
}

