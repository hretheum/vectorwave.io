# Funkcja do parsowania konfiguracji
parse_config() {
    local config=$1
    IFS='|' read -r repo_name visibility description <<< "$config"
    echo "$repo_name" "$visibility" "$description"
}

# Funkcja do tworzenia i inicjalizacji repo
create_and_init_repo() {
    local folder=$1
    local config="${PROJECTS[$folder]}"
    read -r repo_name visibility description <<< $(parse_config "$config")
    
    echo -e "${YELLOW}üì¶ Przetwarzanie: $folder${NC}"
    echo -e "${BLUE}   üìù Nazwa repo: $repo_name${NC}"
    echo -e "${BLUE}   üîí Widoczno≈õƒá: $visibility${NC}"
    echo -e "${BLUE}   üìÑ Opis: $description${NC}"
    
    # Sprawd≈∫ czy repo ju≈º istnieje
    if gh repo view "$GITHUB_USER/$repo_name" &> /dev/null; then
        echo -e "${YELLOW}   ‚ö†Ô∏è  Repo $repo_name ju≈º istnieje, pomijam tworzenie${NC}"
    else
        # Tworzenie repo na GitHub
        echo -e "${BLUE}   üåê Tworzenie repo na GitHub...${NC}"
        if [ "$visibility" == "private" ]; then
            gh repo create "$repo_name" --private --description "$description"
        else
            gh repo create "$repo_name" --public --description "$description"
        fi
        echo -e "${GREEN}   ‚úÖ Utworzono repo: $repo_name${NC}"
    fi
    
    # Inicjalizacja lokalnego repo
    if [ -d "$folder" ]; then
        cd "$folder"
        
        # Sprawd≈∫ czy to ju≈º jest repo git
        if [ -d ".git" ]; then
            echo -e "${YELLOW}   ‚ö†Ô∏è  Folder $folder ju≈º jest repozytorium git${NC}"
            
            # Upewnij siƒô ≈ºe istnieje .gitignore nawet w istniejƒÖcym repo
            if [ ! -f ".gitignore" ]; then
                echo -e "${BLUE}   üìù Tworzenie .gitignore dla istniejƒÖcego repo...${NC}"
                create_comprehensive_gitignore
                echo "" >> .gitignore
                echo "# Project-specific context files" >> .gitignore
                echo "CLAUDE.md" >> .gitignore
                echo "claude.md" >> .gitignore
                git add .gitignore
                git commit -m "Add comprehensive .gitignore with env protection"
            fi
            
            # Instaluj pre-commit hook nawet w istniejƒÖcym repo
            create_pre_commit_hook
            echo -e "${GREEN}   ‚úÖ Zainstalowano pre-commit hook${NC}"
            
            # Dodaj remote je≈õli nie istnieje
            if ! git remote get-url origin &> /dev/null; then
                git remote add origin "git@github.com:$GITHUB_USER/$repo_name.git"
                echo -e "${GREEN}   ‚úÖ Dodano remote origin${NC}"
            fi
        else
            # Nowe repo
            git init
            
            # Tworzenie kompleksowego .gitignore dla subprojektu
            create_comprehensive_gitignore
            
            # Dodaj CLAUDE.md do .gitignore subprojektu
            echo "" >> .gitignore
            echo "# Project-specific context files" >> .gitignore
            echo "CLAUDE.md" >> .gitignore
            echo "claude.md" >> .gitignore
            
            # Instaluj pre-commit hook
            create_pre_commit_hook
            
            # Tworzenie README dla subprojektu
            if [ ! -f "README.md" ]; then
                cat > README.md << EOF
# $repo_name

$description

Part of [Vector Wave](https://github.com/$GITHUB_USER/$MAIN_REPO_NAME) platform.

## Overview

This repository contains the $folder module of the Vector Wave AI content generation platform.

## Setup

See the main [Vector Wave](https://github.com/$GITHUB_USER/$MAIN_REPO_NAME) repository for setup instructions.
EOF
            fi
            
            git add .
            git commit -m "Initial commit: $folder module for Vector Wave platform"
            git branch -M main
            git remote add origin "git@github.com:$GITHUB_USER/$repo_name.git"
            echo -e "${GREEN}   ‚úÖ Zainicjalizowano lokalne repo${NC}"
        fi
        
        # Push do GitHub
        echo -e "${BLUE}   üì§ Push do GitHub...${NC}"
        if git push -u origin main; then
            echo -e "${GREEN}   ‚úÖ Push zako≈Ñczony sukcesem${NC}"
        else
            echo -e "${YELLOW}   ‚ö†Ô∏è  Push nieudany - sprawd≈∫ rƒôcznie${NC}"
        fi
        
        cd ..
    else
        echo -e "${RED}   ‚ùå Folder $folder nie istnieje!${NC}"
    fi
    
    echo ""
}

